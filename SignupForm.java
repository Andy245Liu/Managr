    public String getUsername(){
        return usernameField.getText();
    }
    
    /**
     * Gets user-entered first password
     * 
     * @return String of the entered password
     */
    public String getFirstPassword(){
        char[] passOne = passwordText.getPassword();
        String firstPass = new String(passOne);
        return firstPass;
    }
    
    /**
     * Gets user-entered confirm password
     * 
     * @return String of the entered confirm password
     */
    public String getSecondPassword(){
        char[] passTwo = confirmPasswordText.getPassword();
        String secondPass = new String(passTwo);
        return secondPass;
    }
    
    /**
     * Finds if the sign up form is complete
     * 
     * @return boolean true if it is complete, false if the form is not complete
     */
    public boolean isDone(){
        if(this.getUsername().equals("") || this.getFirstPassword().equals("") || this.getSecondPassword().equals("")){
            
            return false;
        }
        else{ 
            return true;
        }
    }
    
    /**
     * Clears all fields once the user submits
     */
    public void clearAll(){
        usernameField.setText(null);
        passwordText.setText(null);
        confirmPasswordText.setText(null);
        
    }
    
    /**
     * Checks if the username and password are both under 10 characters long
     * @return true if they are, false if they are not
     */
    public boolean validLength(){
        char[] passOne = passwordText.getPassword();
        String firstPass = new String(passOne);
        
        String user = usernameField.getText();
        
        int passLen = firstPass.length();
        int userLen = user.length();
        
        if (passLen <= 10 && userLen <= 10){
            return true;
        } else {
            return false;
        }
        
        
    }
    
    
    
    
    



