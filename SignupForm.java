    /**
     * Method for events once the sign up button is clicked. Adds user account if it is valid, informs user otherwise.
     * @param evt of the sign up button being clicked
     */
    private void signupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupButtonActionPerformed
        // TODO add your handling code here:
        //case if the signup form is not completely filled out
       if (this.isDone() == false){
            JOptionPane.showMessageDialog(this, "You must completely fill in the form", "Error", JOptionPane.ERROR_MESSAGE);
            
        }else{
           
          if(this.validLength() == true){
            SignUpController myController = new SignUpController(this);
            if (myController.reconcilePassword() == true){
                //case if username already exists in the database
                if (myController.userNameExists() == true){
                    JOptionPane.showMessageDialog(this, "Username already exists", "Error", JOptionPane.ERROR_MESSAGE);
                    //case if all is well and the user gets added to the database
                } else{
                    myController.addUser();
                    this.clearAll();
                    LoginForm form= new LoginForm();
                    form.setVisible(true);
                    dispose();
                }
                //case if password does not match confirmation
            } else{
                JOptionPane.showMessageDialog(this, "password does not match", "Error", JOptionPane.ERROR_MESSAGE);
            }
            //case if the username or password are too long
        } else {
           JOptionPane.showMessageDialog(this, "Username and Password must both be 10 characters long or less", "Error", JOptionPane.ERROR_MESSAGE);   
        }
       }
    }//GEN-LAST:event_signupButtonActionPerformed
